import express from 'express';
import morgan from 'morgan';
import router from './routes/api.js';

/////////////////////////////////////////////////////////////
// CREATING THE APP
/////////////////////////////////////////////////////////////
const app = express();
const PORT = process.env.PORT || 8080;

/////////////////////////////////////////////////////////////
// DATA PARSING
/////////////////////////////////////////////////////////////
